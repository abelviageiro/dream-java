package dream.examples.taskBoard;

/**
 * @author Min Yang
 * @date May 13, 2016
 * @description common class for host-var pair
 */
public class Pair {
	String host;
	String Var;

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public String getVar() {
		return Var;
	}

	public void setVar(String var) {
		Var = var;
	}

}
